/// Look up the tone period for a given tone-table index and note number.
pub fn get_note_freq(table: u8, note: u8) -> u16 {
    let n = note.min(95) as usize;
    match table {
        0 => PT3_NOTE_TABLE_PT[n],
        1 => PT3_NOTE_TABLE_ST[n],
        2 => PT3_NOTE_TABLE_ASM[n],
        3 => PT3_NOTE_TABLE_REAL[n],
        _ => PT3_NOTE_TABLE_NATURAL[n],
    }
}

/// Find the note index (1-based) whose frequency most closely matches `env_period / 16`.
pub fn get_note_by_envelope(table: u8, env_period: i32) -> usize {
    let tt: &ToneTable = match table {
        0 => &PT3_NOTE_TABLE_PT,
        1 => &PT3_NOTE_TABLE_ST,
        2 => &PT3_NOTE_TABLE_ASM,
        3 => &PT3_NOTE_TABLE_REAL,
        _ => &PT3_NOTE_TABLE_NATURAL,
    };
    for i in 1..96 {
        let f = (tt[i] as f64 / 16.0).round() as i32;
        if f == env_period {
            return i;
        }
    }
    0
}
